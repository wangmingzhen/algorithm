<?php
//插入排序法，理论上比选择排序法快
$a  = array(8,4,9,2,6,5,10,7);
$n  = count($a);

for ( $i=1;$i<$n;$i++ ){
  for ( $j=$i;$j>0 && $a[$j]<$a[$j-1];$j-- ){
      $a[$j]    = $a[$j]+$a[$j-1];
      $a[$j-1]  = $a[$j]-$a[$j-1];
      $a[$j]    = $a[$j]-$a[$j-1];
  }
}

print_r($a);
